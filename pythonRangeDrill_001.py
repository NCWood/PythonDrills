# Drill One: range() function with one parameter 
for i in range(4):
    print(i)

print("\n")

# Drill Two: range() function with three parameters
for i in range(3,-1,-1):
    print(i)

print("\n")

# Drill Three: range() function with three parameters 8642
for i in range(8,1,-2):
    print(i)

print("\n")



